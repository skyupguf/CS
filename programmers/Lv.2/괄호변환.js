//      - u가 올바른 괄호면 v를 위의 과정으로 재 탐색해 u와 v로 나눠 재귀연산하고 u + v를 리턴한다.
//      - u가 올바른 괄호가 아니면 빈 문자열에 첫 번째 문자로 '('를 붙이고 v를 재귀연산하고 ')'를 붙인다.
//      - u의 첫 번째와 마지막 문자를 제거하고, 나머지 문자열의 괄호 방향을 뒤집어서 뒤에 붙여 v + u를 리턴한다.
