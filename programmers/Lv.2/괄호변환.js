const reBracket = (u) => u.split('').map(e => e === '(' ? ')' : '(').join('');

const makeRightBracket = (p) => {
    if(!p) return p;

    let check = 0, index = 0;
    do {check += p[index++] === '(' ? 1 : -1} while(check !== 0);
    
    const u = p.slice(0, check);
    const v = makeRightBracket(p.slice(check));

    return u[0] === '(' ? u + v 
    : `(${makeRightBracket(v)})${reBracket(u.slice(1, u.length-1))}`;
}
