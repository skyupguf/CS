//  코드#1
const reBracket = (u) => u.split('').map(e => e === '(' ? ')' : '(').join('');

const makeRightBracket = (p) => {
    if(!p) return p;
    let u = '', check = 0, v;
        
    for(let i=0; i<p.length; i++) {
        p[i] === '(' ? check++ : check--;
        u += p[i];
     
        if(!check) {
            v = p.slice(i+1);
            break;
        }
    }
    return u[0] === '(' ? u + makeRightBracket(v)
        : `(${makeRightBracket(v)})${reBracket(u.slice(1, u.length-1))}`;
}
